def plot_performance(results):
    """Plota comparação de performance usando o DataFrame de resultados."""
    df = results

    x = np.arange(len(df))
    bar_width = 0.35

    fig, ax = plt.subplots(figsize=(12, 6))

    train_bars = ax.bar(
        x - bar_width/2,
        df["train_accuracy"],
        bar_width,
        label="Training Accuracy",
        color=COLORS["blue"]
    )

    val_bars = ax.bar(
        x + bar_width/2,
        df["val_accuracy"],
        bar_width,
        label="Validation Accuracy",
        color=COLORS["dark"]
    )

    ax.set_xlabel("Model", fontsize=12, loc="left")
    ax.set_ylabel("Accuracy", fontsize=12, loc="top")

    ax.set_title(
        "Model Performance Comparison",
        fontsize=14,
        fontweight="bold",
        loc="left",
        pad=20
    )

    ax.set_xticks(x)
    ax.set_xticklabels(df["model"], rotation=45, ha="right")

    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    ax.grid(axis="y", alpha=0.3)

    for bar in train_bars + val_bars:
        height = bar.get_height()
        ax.annotate(
            f"{height:.2f}",
            xy=(bar.get_x() + bar.get_width()/2, height),
            xytext=(0, 3),
            textcoords="offset points",
            ha="center",
            va="bottom",
            fontsize=9
        )

    ax.legend(loc="lower right")

    plt.tight_layout()
    plt.show()


def plot_confusion_matrices(trained_models, X_val, y_val):
    """Plota matrizes de confusão para cada modelo treinado."""
    n = len(trained_models)

    fig, axes = plt.subplots(1, n, figsize=(5*n, 4))

    if n == 1:
        axes = [axes]

    for ax, (name, model) in zip(axes, trained_models.items()):
        y_pred = model.predict(X_val)
        cm = confusion_matrix(y_val, y_pred)

        sns.heatmap(
            cm,
            annot=True,
            fmt="d",
            cmap="Blues",
            ax=ax,
            cbar_kws={"label": "Count"}
        )

        ax.set_title(f"Confusion Matrix - {name}", fontweight="bold")
        ax.set_xlabel("Predicted")
        ax.set_ylabel("Actual")
        ax.set_xticklabels(["Not Survived", "Survived"])
        ax.set_yticklabels(["Not Survived", "Survived"])

    plt.tight_layout()
    plt.show()


def plot_roc_curves(trained_models, X_val, y_val):
    """Plota curvas ROC para cada modelo treinado."""
    fig, ax = plt.subplots(figsize=(8, 6))

    colors = [
        COLORS["dark"],
        COLORS["blue"],
        COLORS["light_blue"],
        COLORS["soft"]
    ]

    for i, (name, model) in enumerate(trained_models.items()):
        y_proba = model.predict_proba(X_val)[:, 1]
        fpr, tpr, _ = roc_curve(y_val, y_proba)
        auc_score = roc_auc_score(y_val, y_proba)

        ax.plot(
            fpr, tpr, linewidth=2,
            color=colors[i % len(colors)],
            label=f"{name} (AUC={auc_score:.3f})"
        )

    ax.plot(
        [0, 1], [0, 1], linestyle="--",
        color=COLORS["orange"], linewidth=2,
        label="Random Classifier"
    )

    ax.set_xlabel("False Positive Rate")
    ax.set_ylabel("True Positive Rate")
    ax.set_title("ROC Curves - Model Comparison", fontweight="bold")
    ax.legend(loc="lower right")
    ax.grid(alpha=0.3)

    plt.tight_layout()
    plt.show()


def print_classification_reports(trained_models, X_val, y_val):
    """Imprime classification report para cada modelo."""
    for name, model in trained_models.items():
        y_pred = model.predict(X_val)
        print(f"\n{name}")
        print("-" * 40)
        print(classification_report(y_val, y_pred))